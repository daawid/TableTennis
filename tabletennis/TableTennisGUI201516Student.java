    public TableTennisGUI201516Student() {
        initComponents();

        Scanner tScan = new Scanner(TableTennisGUI201516Student.class.getResourceAsStream("/teams.txt"));
        Scanner pScan = new Scanner(TableTennisGUI201516Student.class.getResourceAsStream("/players.txt"));

        while (tScan.hasNextLine()) {
            String lineData = tScan.nextLine();
            regManager.createAndAddTeam(lineData);
        }
        tScan.close();

        while (pScan.hasNextLine()) {
            String lineData[] = pScan.nextLine().split(",");
            regManager.createAndAttachPlayer(lineData[1], lineData[0]);
        }
        pScan.close();

        initTeamComboBox();
        initHomeAwayComboBox();

    }

