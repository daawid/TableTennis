    private void calculateAndSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateAndSubmitButtonActionPerformed
        String hTeam = (String) homeTeamComboBox.getSelectedItem();
        String aTeam = (String) awayTeamComboBox.getSelectedItem();

        populateScoresArray();
        updatePlayerCombobox();

        Match m = fixtures.getAMatch(hTeam, aTeam);

        m.sethPlayer1(hplayer1);
        m.sethPlayer2(hplayer2);
        m.setaPlayer1(aplayer1);
        m.setaPlayer2(aplayer2);

        m.setScoreSet(scoreSet);

        System.out.println(hplayer1 + " " + hplayer2 + " " + aplayer1 + " " + aplayer2);

        int si = -1;
        for (Set s : m.getSets()) {

            s.enterSetScore((scoreSet[++si]), (scoreSet[++si]),
                    (scoreSet[++si]), (scoreSet[++si]),
                    (scoreSet[++si]), (scoreSet[++si]));

        }

        m.calculateScores();
        teamTotalsField.setText(Integer.toString(m.gethTeamWin()) + ":" + Integer.toString(m.getaTeamWin()));


    }//GEN-LAST:event_calculateAndSubmitButtonActionPerformed

    private void populateScoresArray() {
        scoreSet[0] = getFirst(h1a1Field1.getText()); //h1
        scoreSet[1] = getSecond(h1a1Field1.getText()); //a1
        scoreSet[2] = getFirst(h1a1Field2.getText()); //h2
        scoreSet[3] = getSecond(h1a1Field2.getText()); //a2
        scoreSet[4] = getFirst(h1a1Field3.getText()); //h3
        scoreSet[5] = getSecond(h1a1Field3.getText()); //a3

        scoreSet[6] = getFirst(h1a2Field1.getText());
        scoreSet[7] = getSecond(h1a2Field1.getText());
        scoreSet[8] = getFirst(h1a2Field2.getText());
        scoreSet[9] = getSecond(h1a2Field2.getText());
        scoreSet[10] = getFirst(h1a2Field3.getText());
        scoreSet[11] = getSecond(h1a2Field3.getText());

        scoreSet[12] = getFirst(h2a1Field1.getText());
        scoreSet[13] = getSecond(h2a1Field1.getText());
        scoreSet[14] = getFirst(h2a1Field2.getText());
        scoreSet[15] = getSecond(h2a1Field2.getText());
        scoreSet[16] = getFirst(h2a1Field3.getText());
        scoreSet[17] = getSecond(h2a1Field3.getText());

        scoreSet[18] = getFirst(h2a2Field1.getText());
        scoreSet[19] = getSecond(h2a2Field1.getText());
        scoreSet[20] = getFirst(h2a2Field2.getText());
        scoreSet[21] = getSecond(h2a2Field2.getText());
        scoreSet[22] = getFirst(h2a2Field3.getText());
        scoreSet[23] = getSecond(h2a2Field3.getText());

        scoreSet[24] = getFirst(doubleField1.getText());
        scoreSet[25] = getSecond(doubleField1.getText());
        scoreSet[26] = getFirst(doubleField2.getText());
        scoreSet[27] = getSecond(doubleField2.getText());
        scoreSet[28] = getFirst(doubleField3.getText());
        scoreSet[29] = getSecond(doubleField3.getText());
    }

    private int getFirst(String input) {
        String[] parts = input.split(":");
        return Integer.parseInt(parts[0]);
    }

    private int getSecond(String input) {
        String[] parts = input.split(":");
        return Integer.parseInt(parts[1]);
    }

