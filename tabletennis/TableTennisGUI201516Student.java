    private void initHomeAwayComboBox() {
        String homeTeam = (String) homeTeamComboBox.getSelectedItem();
        Team hTeam = (regManager.getATeam(homeTeam));
        String[] playersarray = hTeam.listMembers().toArray(new String[hTeam.listMembers().size()]);

        System.out.println(Arrays.toString(playersarray));

        homePlayer1Combo.setModel(new javax.swing.DefaultComboBoxModel(playersarray));
        homePlayer2Combo.setModel(new javax.swing.DefaultComboBoxModel(playersarray));

        String awayTeam = (String) awayTeamComboBox.getSelectedItem();
        Team aTeam = (regManager.getATeam(awayTeam));
        String[] aplayersarray = aTeam.listMembers().toArray(new String[aTeam.listMembers().size()]);

        System.out.println(Arrays.toString(aplayersarray));

        awayPlayer1Combo.setModel(new javax.swing.DefaultComboBoxModel(aplayersarray));
        awayPlayer2Combo.setModel(new javax.swing.DefaultComboBoxModel(aplayersarray));
    }

    private void initTeamComboBox() {
        ArrayList<String> teamNames = new ArrayList<>();
        for (Team t : regManager.getTeams()) {
            teamNames.add(t.getTname());
        }

        homeTeamComboBox.setModel(new javax.swing.DefaultComboBoxModel(teamNames.toArray()));
        awayTeamComboBox.setModel(new javax.swing.DefaultComboBoxModel(teamNames.toArray()));
        regTeamsCombo.setModel(new javax.swing.DefaultComboBoxModel(teamNames.toArray()));

    }

