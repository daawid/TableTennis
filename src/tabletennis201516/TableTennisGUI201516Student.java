    /**
     * Adds a split to the set scores *
     */
    private int First(String input) {
        String[] parts = input.split(":");
        return Integer.parseInt(parts[0]);
    }

    private int Second(String input) {
        String[] parts = input.split(":");
        return Integer.parseInt(parts[1]);
    }

