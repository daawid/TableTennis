    private void viewMatchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewMatchButtonActionPerformed
        String hteam = JOptionPane.showInputDialog("Enter the home team");
        String ateam = JOptionPane.showInputDialog("Enter the away team");
        String s = "";
        viewerTextArea.setText(s);

        /**
         * Looks up the match based on the home and away team and displays all
         * the match data using the code below i.e. getSets and getGames
         */
        Match m = fixtures.getAMatch(hteam, ateam);
        s += m.toString();
        s += System.lineSeparator();
        s += "SingleSets";
        for (int i = 0; i < 4; i++) {
            s += m.getSets()[i].toString() + m.getSets()[i].getGames().get(0).toString() + ","
                    + m.getSets()[i].getGames().get(1).toString() + ","
                    + m.getSets()[i].getGames().get(2).toString() + "}";
            s += System.lineSeparator();
        }
        s += "Double set: Set{null vs null = " + m.getDoubleSet().getGames().get(0).toString() + ","
                + m.getDoubleSet().getGames().get(1).toString() + ","
                + m.getDoubleSet().getGames().get(2).toString() + "}";
        s += System.lineSeparator();
        s += "final scores: " + m.getHTeamWin() + ":" + m.getATeamWin();

        viewerTextArea.append(s);

    }//GEN-LAST:event_viewMatchButtonActionPerformed

